"""# **Define recall@10 helper function**"""

def recall_at_k(retrieved, ground_truth, k=10):
    hits = 0
    for r, g in zip(retrieved, ground_truth):
        hits += len(set(r[:k]) & set(g[:k]))
    return hits / (len(retrieved) * k)

